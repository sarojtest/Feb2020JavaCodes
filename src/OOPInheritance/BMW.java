package OOPInheritance;

public class BMW extends Car{
	
	//Method Overriding
	@Override
	public void start(){
		System.out.println("BMW -- start");
	}
	
	public void autoParking(){
		System.out.println("BMW -- auto parking");
	}
	
	public void theftSafety(){
		System.out.println("BMW -- theftSafety");

	}
	
	
	
	

}
